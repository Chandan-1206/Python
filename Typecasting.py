# Explicit typecasting
a="50"
b="3"
print ("the value of a + b is :",a+b,"concatination is being done as both are strings" )
c="50"
d="3"
print ("the value of c + d is :",int(c)+int(d),"c and d are typecasted into int")

# implicit typecasting
e=2
f=3.46
print("the value of e + f is :",e+f,"e is being typecasted into float by compiler")
